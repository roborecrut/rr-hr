
-- Admin RPCs for managing job_titles templates
CREATE OR REPLACE FUNCTION public.admin_list_job_titles()
RETURNS TABLE (
  id uuid,
  title text,
  title_norm text,
  usage_count integer,
  is_basic boolean,
  field_templates jsonb,
  created_at timestamptz
)
LANGUAGE plpgsql
STABLE SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NOT public.has_role(auth.uid(), 'admin'::public.app_role) THEN
    RAISE EXCEPTION 'forbidden';
  END IF;
  RETURN QUERY
    SELECT j.id, j.title, j.title_norm, j.usage_count, j.is_basic,
           COALESCE(j.field_templates,'{}'::jsonb), j.created_at
      FROM public.job_titles j
      ORDER BY j.usage_count DESC NULLS LAST, j.title ASC;
END $$;

CREATE OR REPLACE FUNCTION public.admin_job_title_upsert_templates(
  _title text,
  _patch jsonb,
  _overwrite boolean DEFAULT true,
  _is_basic boolean DEFAULT NULL
) RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_norm text := lower(btrim(_title));
  v_row public.job_titles;
  v_existing jsonb;
  v_merged jsonb;
  k text;
BEGIN
  IF NOT public.has_role(auth.uid(), 'admin'::public.app_role) THEN
    RAISE EXCEPTION 'forbidden';
  END IF;
  IF v_norm IS NULL OR v_norm = '' THEN RAISE EXCEPTION 'empty_title'; END IF;
  IF _patch IS NULL OR jsonb_typeof(_patch) <> 'object' THEN
    RAISE EXCEPTION 'bad_patch';
  END IF;

  SELECT * INTO v_row FROM public.job_titles WHERE title_norm = v_norm LIMIT 1;
  IF v_row.id IS NULL THEN
    INSERT INTO public.job_titles (title, usage_count, is_basic, created_by, field_templates)
    VALUES (btrim(_title), 0, COALESCE(_is_basic,false), auth.uid(), '{}'::jsonb)
    RETURNING * INTO v_row;
  END IF;

  v_existing := COALESCE(v_row.field_templates, '{}'::jsonb);
  IF _overwrite THEN
    v_merged := v_existing || _patch;
  ELSE
    v_merged := v_existing;
    FOR k IN SELECT jsonb_object_keys(_patch) LOOP
      IF NOT (v_merged ? k) OR COALESCE(v_merged->>k,'') = '' THEN
        IF COALESCE(_patch->>k,'') <> '' THEN
          v_merged := v_merged || jsonb_build_object(k, _patch->k);
        END IF;
      END IF;
    END LOOP;
  END IF;

  UPDATE public.job_titles
    SET field_templates = v_merged,
        is_basic = COALESCE(_is_basic, is_basic)
    WHERE id = v_row.id;
  RETURN v_merged;
END $$;

CREATE OR REPLACE FUNCTION public.admin_delete_job_title(_id uuid)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NOT public.has_role(auth.uid(), 'admin'::public.app_role) THEN
    RAISE EXCEPTION 'forbidden';
  END IF;
  DELETE FROM public.job_titles WHERE id = _id;
  RETURN jsonb_build_object('ok', true);
END $$;

-- Seed 20 popular positions with starter templates.
-- Helper: insert or merge field_templates for a title (only fills empty keys to be safe).
CREATE OR REPLACE FUNCTION public._seed_job_title(_title text, _tpl jsonb)
RETURNS void
LANGUAGE plpgsql
SET search_path = public
AS $$
DECLARE
  v_norm text := lower(btrim(_title));
  v_row public.job_titles;
  v_existing jsonb;
  v_merged jsonb;
  k text;
BEGIN
  SELECT * INTO v_row FROM public.job_titles WHERE title_norm = v_norm LIMIT 1;
  IF v_row.id IS NULL THEN
    INSERT INTO public.job_titles (title, usage_count, is_basic, field_templates)
    VALUES (btrim(_title), 0, true, _tpl);
    RETURN;
  END IF;
  v_existing := COALESCE(v_row.field_templates, '{}'::jsonb);
  v_merged := v_existing;
  FOR k IN SELECT jsonb_object_keys(_tpl) LOOP
    IF NOT (v_merged ? k) OR COALESCE(v_merged->>k,'') = '' THEN
      v_merged := v_merged || jsonb_build_object(k, _tpl->k);
    END IF;
  END LOOP;
  UPDATE public.job_titles SET field_templates = v_merged, is_basic = true WHERE id = v_row.id;
END $$;

