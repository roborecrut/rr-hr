const LEGACY_MAP: Record<string, MascotContext> = {
  company: "field_improve",
  vacancy: "field_improve",
  training: "gen_material",
  interview: "checklist_grade",
  profile: "universal",
  crm: "universal",
  billing: "universal",
  error: "universal",
  saved: "universal",
  ai_wait: "universal",
  ai_done: "final_result",
  generic: "universal",
};

export const LOADING_PHRASES: Record<LoadingEntity, string[]> = new Proxy(
  {} as Record<LoadingEntity, string[]>,
  {
    get(_t, key: string) {
      if (key in MASCOT_PHRASES) return MASCOT_PHRASES[key as MascotContext];
      const mapped = LEGACY_MAP[key] || "universal";
      return MASCOT_PHRASES[mapped];
    },
  },
);

export function pickPhrase(entity: LoadingEntity, idx: number): string {
  const arr = (LOADING_PHRASES as any)[entity] || MASCOT_PHRASES.universal;
  return arr[idx % arr.length];
}