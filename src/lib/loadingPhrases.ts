// --- Резолвер контекста по заголовку операции -----------------------------

const TITLE_RULES: Array<{ re: RegExp; ctx: MascotContext }> = [
  { re: /распознаван|читает файл|разбор ссылк|разносит данные/i, ctx: "doc_ingest" },
  { re: /оценка резюме|резюме/i, ctx: "resume_grade" },
  { re: /чек.?лист/i, ctx: "checklist_grade" },
  { re: /ролев|ситуац/i, ctx: "situations_grade" },
  { re: /оценка ответа/i, ctx: "role_answer" },
  { re: /генерац.*(вопрос|критер|чек)/i, ctx: "gen_questions" },
  { re: /генерац.*(ситуац|ролев)/i, ctx: "gen_situations" },
  { re: /генерац.*материал|учебн/i, ctx: "gen_material" },
  { re: /генерац.*тест|тест по материал/i, ctx: "gen_test" },
  { re: /проверка ответов|проверка экзамен/i, ctx: "training_check" },
  { re: /итог|финал/i, ctx: "final_result" },
  { re: /улучшает|упаковыв|редактир/i, ctx: "field_improve" },
];

export function resolveContext(title: string | undefined | null): MascotContext {
  if (!title) return "universal";
  for (const r of TITLE_RULES) if (r.re.test(title)) return r.ctx;
  return "universal";
}

// --- Очередь реплик: перемешать один раз, не повторять подряд -------------

function shuffle<T>(src: readonly T[]): T[] {
  const a = src.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

/**
 * Строит перемешанную очередь реплик для контекста. Если общая длина
 * меньше minLen — подмешивает универсальные реплики в хвост.
 */
export function buildPhraseQueue(ctx: MascotContext, minLen: number = 16): string[] {
  const primary = shuffle(MASCOT_PHRASES[ctx] || MASCOT_PHRASES.universal);
  if (primary.length >= minLen || ctx === "universal") return primary;
  const filler = shuffle(MASCOT_PHRASES.universal).filter((p) => !primary.includes(p));
  return primary.concat(filler).slice(0, Math.max(minLen, primary.length));
}

/** Общее число уникальных реплик (для отчёта / тестов). */
export function totalUniquePhrases(): number {
  const set = new Set<string>();
  for (const arr of Object.values(MASCOT_PHRASES)) for (const p of arr) set.add(p);
  return set.size;
}

